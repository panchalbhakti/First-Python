list = [2, 1, 3]
print(list)
list.append(4) #adds one element at the end
print(list)
list.sort() #sort the list in ascending order
print(list)
list.sort(reverse=True) #sort in descending order
print(list)
list.reverse() #reverses list
print(list)
list.insert(1, 5) # list.insert(idx, ele)
print(list)
list.remove(1) #removes element 1 from the list
print(list)
list.pop(0) #list.pop(idx)
print(list)